module.exports = async function handler(req, res) {
    if (!BOT_TOKEN) return res.json({ ok: false, error: 'DIAMOND_BOT_TOKEN not set' });

    try {
        var { data: subs, error } = await supabase
            .from('diamond_bot_subscribers')
            .select('*')
            .eq('is_active', true);

        if (error || !subs) return res.json({ ok: false, error: 'DB error' });

        var now = new Date();
        var sent = 0, skipped = 0, blocked = 0;

        for (var i = 0; i < subs.length; i++) {
            var sub = subs[i];
            if (!sub.telegram_chat_id || !sub.started_at) { skipped++; continue; }

            var startDate = new Date(sub.started_at);
            var hoursSinceStart = (now - startDate) / (1000 * 60 * 60);
            var lastDaySent = sub.last_day_sent || 0;

            // Ищем следующий день по часам
            var nextDay = null;
            for (var d = 0; d < SEND_DAYS.length; d++) {
                var day = SEND_DAYS[d];
                if (day > lastDaySent && hoursSinceStart >= MIN_HOURS[day]) {
                    nextDay = day;
                    break;
                }
            }

            if (!nextDay) { skipped++; continue; }

            var msg = getMsg(nextDay, sub.telegram_first_name);
            if (!msg) { skipped++; continue; }

            var opts = (msg.btn_text && msg.btn_url) ? { btn_text: msg.btn_text, btn_url: msg.btn_url } : {};
            var result = await sendMsg(sub.telegram_chat_id, msg.text, opts);

            if (result === 'blocked') {
                await supabase.from('diamond_bot_subscribers').update({ is_active: false }).eq('telegram_id', sub.telegram_id);
                blocked++;
            } else {
                await supabase.from('diamond_bot_subscribers').update({ last_day_sent: nextDay, last_message: now.toISOString() }).eq('telegram_id', sub.telegram_id);
                sent++;
            }

            await new Promise(function(r) { setTimeout(r, 200); });
        }

        console.log('📨 Diamond Sequence v2.0: sent=' + sent + ' skip=' + skipped + ' blocked=' + blocked);
        return res.json({ ok: true, sent: sent, skipped: skipped, blocked: blocked });

    } catch(e) {
        console.error('Diamond Sequence error:', e);
        return res.json({ ok: false, error: e.message });
    }
};
