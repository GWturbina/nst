const { createClient } = require('@supabase/supabase-js');

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_KEY || process.env.SUPABASE_ANON_KEY;
const BOT_TOKEN = process.env.DIAMOND_BOT_TOKEN;

const supabase = createClient(supabaseUrl, supabaseKey);

async function sendMsg(chatId, text, opts) {
    var body = { chat_id: chatId, text: text, parse_mode: 'HTML' };
    if (opts && opts.btn_text && opts.btn_url) {
        body.reply_markup = { inline_keyboard: [[{ text: opts.btn_text, url: opts.btn_url }]] };
    }
    try {
        var r = await fetch('https://api.telegram.org/bot' + BOT_TOKEN + '/sendMessage', {
            method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(body)
        });
        var d = await r.json();
        if (!d.ok && d.description && d.description.includes('blocked')) return 'blocked';
        return 'sent';
    } catch(e) { return 'error'; }
}

